#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	ret;

	i = 0;
	while (dst[i] != '\0')
		i++;
	ret = 0;
	while (src[ret] != '\0')
		ret++;
	if (size <= i)
		ret += size;
	else
		ret += i;
	j = 0;
	while (src[j] != '\0' && i + 1 < size)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (ret);
}
