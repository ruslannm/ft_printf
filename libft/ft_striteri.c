void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	char			*tmp;
	unsigned int	i;

	if (!s || !f)
		return ;
	i = 0;
	tmp = s;
	while (tmp[i])
	{
		f(i, &tmp[i]);
		i++;
	}
}
