#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char *s_tmp;

	s_tmp = (unsigned char *)s;
	while (n-- > 0)
	{
		*s_tmp = 0;
		s_tmp++;
	}
}
